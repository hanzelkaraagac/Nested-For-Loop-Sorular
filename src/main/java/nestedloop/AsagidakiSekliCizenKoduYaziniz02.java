package nestedloop;

public class AsagidakiSekliCizenKoduYaziniz02 {
    public static void main(String[] args) {

         /*

                  //Example 3: Asagidaki sekli cizen kodu yaziniz

                  * * * *
                  * * *
                  * *
                  *


         */

        for ( int i =4; i>0; i--) {

            for (int j =1; j<=i; j++) {

                System.out.print("*" + " ");
            }
            System.out.println();
        }

    }
}
